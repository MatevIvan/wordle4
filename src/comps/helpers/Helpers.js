export function testLetter(selectedWord, letter, pos) {
  if (letter !== null) {
    // if the letter is not in the word
    if (!selectedWord.includes(letter)) {
      return "wrong-letter";
    }
    if (selectedWord.split("").find((tmp, i) => i === pos && tmp === letter)) {
      return "correct-letter";
    }
    if (selectedWord.includes(letter)) {
      return "warning-letter";
    }
  }

  return null;
}

